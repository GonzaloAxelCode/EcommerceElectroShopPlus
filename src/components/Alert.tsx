type Props = {
  showAlert?: any;
  color?: any;
  message?: any;
  cancel_alert?: Function;
};

const Alert: FC<Props> = ({ showAlert, color, message, cancel_alert }) => {
  if (!showAlert) {
    return <></>;
  }

  if (color === "green") {
    return <Success message={message} cancel_alert={cancel_alert} />;
  }
  if (color === "red") {
    return <Fail message={message} cancel_alert={cancel_alert} />;
  }
  if (color === "blue") {
    return <Updated message={message} cancel_alert={cancel_alert} />;
  }
  return <></>;
};

